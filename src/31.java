public class MathProblems {
    public static void main(String[] args) {
        // Example 1: Calculate the sum of two numbers
        int number1 = 5;
        int number2 = 3;
        int result = number1 + number2;
        System.out.println("The sum is: " + result);

        // Example 2: Multiply two numbers
        double number3 = 4.5;
        double number4 = 2.0;
        double result2 = number3 * number4;
        System.out.println("The product is: " + result2);

        // Example 3: Calculate the difference of two numbers
        int result3 = 10 - 3;
        System.out.println("The difference is: " + result3);

        // Example 4: Find the square root of a number
        double number5 = 9.0;
        double result4 = Math.sqrt(number5);
        System.out.println("The square root is: " + result4);
    }
}
